package 二叉搜索树相关.q450_删除二叉搜索树中的节点_medium.递归.q1325_删除给定值的叶子节点_medium;

public class TreeNode {
    int val;
    TreeNode left;
    TreeNode right;

    TreeNode() {
    }

    TreeNode(int val) {
        this.val = val;
    }

    TreeNode(int val, TreeNode left, TreeNode right) {
        this.val = val;
        this.left = left;
        this.right = right;
    }
}
